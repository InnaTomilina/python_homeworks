# output - several parameters separated by %%%
print(1, 5, 234, "a", sep="%%%")

# output - several parameters separated by commas
print("a", "b", "c", "d", sep=",")

# output - second print statement will be printed on a new line and indented by one line with "!" in the end
print('Hello, Beetroot academy', end='\n\n')
print('Hello, Beetroot academy', end='!')
